package DTO;

public class CaixaDTO {
	
	private float lucroDiario;
	private float lucroMensal;
	private float valorDeCaixa;
	private int mes;
	
	public float getLucroDiario() {
		return lucroDiario;
	}
	public void setLucroDiario(float lucroDiario) {
		this.lucroDiario = lucroDiario;
	}
	public float getLucroMensal() {
		return lucroMensal;
	}
	public void setLucroMensal(float lucroMensal) {
		this.lucroMensal = lucroMensal;
	}
	public float getValorDeCaixa() {
		return valorDeCaixa;
	}
	public void setValorDeCaixa(float valorDeCaixa) {
		this.valorDeCaixa = valorDeCaixa;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	
}
